		<h2><a id="7">7. Pelin idea</a></h2>
			<p class = "content">
				Pelaaja on "eeropsykoosiin" vajonnut henkilö joka löytää itsensä pimeästä</br>
				labyrintistä jossa kaikki mahdollinen muistuttaa häntä Eerosta.</br></br>
				
				Pelaajan tehtävänä on löytää labyrintistä mystinen Eerokuutio ennen kuin</br>
				labyrintissa lurkaava, pelaajaa etsivä demonieero löytää pelaajan.</br></br>
			</p>
			
