		<h2><a id="12">12. Luokat (v0.1)</a></h2>
			<h3>12.1 Game</h3>
				<p class = "content">
					Game-luokka on pelin ns. pääluokka jossa on pelin ydintoiminnot kuten</br>
					ikkunan luonti, ohjelman tilan hallinta, pelin päivitys ja pelin piirto.</br></br>
					
					Game-luokka lataa ohjelman resurssit, kuten esimerkiksi kuvat ja äänet muistiin</br>
					sekä luo pelin toiminnan kannalta tarvittavat oliot (esim. Maze-olio, viholliset jne.).
				</p>
			<h3>12.2 Maze</h3>
				<p class = "content">
					Maze-luokka vastaa labyrintin generoimisesta peliin käyttäen hyväkseen
					<a href = "http://en.wikipedia.org/wiki/Maze_generation_algorithm#Depth-first_search">Depth-first searchia</a>,</br>
					jonka jälkeen algoritmin luomasta labyrintista karsitaan vielä erikseen ylimääräisiä seiniä halutun lopputuloksen</br>
					saamiseksi.
				</p>
			<h3>12.3 Cube</h3>
				<p class = "content">
					Cube itsessään on pelin "maali", eli eerokuutio jonka luokse pelaajan tulee päästä.</br>
					Cuben luonnin yhteydessä se sijoitetaan satunnaisesti labyrintin sisään tietyn minimimatkan päähän pelaajasta.</br></br>
					
					Luokassa itsessään ei ole sen kummempia ominaisuuksia kuution luonnin, sijoittelun ja pyörittelyn lisäksi.</br>
					Kuutio lisäksi hymisee mystisesti, jotta pelaaja pystyy paikallistamaan sen äänen perusteella.</br>
					Cube-luokasta perii luokka Enemy, josta lisää seuraavaksi.
				</p>
			<h3>12.4 Enemy</h3>
				<p class = "content">
					Enemy-luokka on Cube-luokasta peritty luokka, jonka toimintaa on muunneltu</br>
					siten että siitä on saatu peliin "vihollinen", eli demonieero.</br></br>
					
					Enemy eroaa Cubesta siten että se "etsii" tai lähestyy pelaajaa labyrintissä. Mikäli</br>
					se saa pelaajan kiinni niin peli päättyy häviöön. Labyrintin ulkomuoto alkaa </br>
					muuttumaan kun vihollinen on lähettyvillä, jotta pelaaja tietää milloin vaara uhkaa </br>
					Labyrinttiin ilmestyy uusi Enemy tietyn väliajoin</br>
				</p>
			<h3>12.5 Menu</h3>
