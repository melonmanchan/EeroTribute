		<h2><a id="4">4. Yleisesti</a></h2>
			<p class = "content">
				Eerotribute3D on JAMKin Windows-ohjelmointi(IIO11300) -kurssille tehty harjoitustyö, jonka </br>
				tavoitteena ei ole vain ohjelmoida näyttävä 3D windows-peli, mutta myös ilmaista </br>
				ikävämme ryhmästämme toiseen kouluun ja paikkakuntaan lähteneeseen toveriin, Eeroon.</br></br>
				
				Eerotribute3D on saannut inspiraationsa Eeron lähtemisestä, lukuisista markkinoilla olevista</br>
				kauhupeleistä sekä luonnollisesti Eerotribute3D:n edeltäjästä; Eerotribute2D:stä.
			</p>
		
